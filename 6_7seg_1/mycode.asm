; Proteus code, 7SD
CODE SEGMENT
    
START:
 MOV AL, 080H
 OUT 006H, AL ; CR REGISTER ADDRESS = 0F6H, VALUE = 080H
 
X:
 
  MOV AL, 0F9H ;DISPLAYING '1''
  OUT 000H,AL ; PORT A ADDRESS

   
  MOV AL, 0A4H ;DISPLAYING '2'
  OUT 002H,AL ; PORT B ADDRESS 
  
   
  
  MOV AL, 0B0H ;DISPLAYING '3''
  OUT 004H,AL ; PORT C ADDRESS
  
  MOV CX, 0FFFFH ;DELAY
  L1: LOOP L1
  
  MOV AL, 099H ;DISPLAYING '4'
  OUT 000H,AL ; PORT A ADDRESS 
   
  MOV AL, 092H ;DISPLAYING '5'
  OUT 002H,AL ; PORT B ADDRESS
   
  MOV AL, 082H ;DISPLAYING '6'
  OUT 004H,AL ; PORT C ADDRESS
  
  MOV CX, 0FFFFH ;DELAY
  L2: LOOP L2 
  

             
  
  
  
  
JMP X

CODE ENDS
END




